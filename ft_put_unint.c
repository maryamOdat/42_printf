#include "ft_printf.h"

int	ft_put_unint(unsigned int n, int *counter)
{
	if (n >= 10)
	{
		ft_put_unint(n / 10, counter);
	}
	*counter += ft_putchar((n % 10) + '0');
	return (0);
}
