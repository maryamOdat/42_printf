#include "ft_printf.h"
#include <unistd.h>

int	ft_putstr(char const *s)
{
	size_t	i;

	i = 0;
	if (s == NULL)
		return (ft_putstr("(null)"));
	while (s[i] != '\0')
	{
		write(1, &s[i++], 1);
	}
	return (i);
}
