#include "ft_printf.h"

int	ft_upper_hex(unsigned long n, int *counter)
{
	if (n >= 16)
	{
		ft_upper_hex(n / 16, counter);
	}
	*counter += ft_putchar("0123456789ABCDEF"[n % 16]);
	return (0);
}
